// condicionais
const a = 10;

if (a == '10') {
    console.log("a é igual a 10");
} else {
    console.log("a é diferente de 10");
}
// == compara valores, mas não tipos. É uma comparação fraca.
// === compara valores e tipos. É uma comparação forte.

const numb = a === 10 ? "a é igual a 10" : "a é diferente de 10";

console.log(numb);

